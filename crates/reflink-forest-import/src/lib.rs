//! Imports a fixed Git ref snapshot into the cold store.
//!
//! The backend resolves refs once, walks immutable OIDs, and feeds raw object
//! payloads to the serialized cold-store writer. The source repository can be
//! removed after callers persist the returned snapshot metadata.

use std::{
    fs::{self, File, OpenOptions},
    io::{self, Read, Write},
    path::Path,
    process,
    time::{SystemTime, UNIX_EPOCH},
};

use reflink_forest_core::{ContentId, GitOid, HashAlgorithm};
use reflink_forest_format::{Codec, ObjectRecord};
use reflink_forest_git::{GitBackend, GitBackendError, LocalRefKind, RefSnapshot};
use reflink_forest_index::{Catalog, RepoId};
use reflink_forest_store::{ChunkWriter, StoreError};

/// The current binary encoding version for repository snapshot manifests.
pub const SNAPSHOT_MANIFEST_VERSION: u16 = 1;
/// Maximum accepted manifest size. This bounds parsing allocations for an
/// untrusted or partially written manifest.
pub const MAX_SNAPSHOT_MANIFEST_BYTES: usize = 16 * 1024 * 1024;
/// A repository snapshot ID is generated by the daemon rather than inferred
/// from mutable refs. It is deliberately distinct from a repository ID.
pub const REPO_SNAPSHOT_ID_LEN: usize = reflink_forest_index::SNAPSHOT_ID_LEN;

/// Import-layer name for the catalog's stable repository-snapshot ID.
///
/// Re-exporting the catalog type keeps durable manifest identity identical to
/// the ID used by visibility and GC-root records.
pub use reflink_forest_index::SnapshotId as RepoSnapshotId;

const SNAPSHOT_MANIFEST_MAGIC: &[u8; 8] = b"RFSNAP\0\0";
const MANIFEST_REF_NAME_MAX_BYTES: usize = 64 * 1024;
const MANIFEST_TOOL_VERSION_MAX_BYTES: usize = 64 * 1024;
const MANIFEST_OPTIONAL_FIELD_MAX_BYTES: usize = 4 * 1024 * 1024;

/// One byte-preserving local ref retained by a completed snapshot manifest.
#[derive(Clone, Debug, Eq, PartialEq)]
pub struct PersistedRef {
    pub name: Vec<u8>,
    pub kind: LocalRefKind,
    pub target: GitOid,
}

/// Optional manifest data that this version does not otherwise interpret.
///
/// Unknown optional fields are retained verbatim on load and re-emitted on
/// write, so a v1 tool does not silently discard later optional metadata.
#[derive(Clone, Debug, Eq, PartialEq)]
pub struct OptionalManifestField {
    pub tag: u16,
    pub value: Vec<u8>,
}

/// The exact ref-selection policy used for a snapshot import.
#[derive(Clone, Copy, Debug, Eq, PartialEq)]
pub enum ImportPolicy {
    /// Import direct tips under `refs/heads/*` and `refs/tags/*` only.
    LocalBranchesAndTags,
}

/// A self-contained, durable description of a completed local-ref import.
///
/// This is deliberately separate from catalog publication. Callers write and
/// synchronize this manifest first, then make the corresponding snapshot
/// `Ready` in their catalog transaction. A manifest alone is never proof that
/// a snapshot is ready for user visibility.
#[derive(Clone, Debug, Eq, PartialEq)]
pub struct SnapshotManifest {
    pub repository: RepoId,
    pub snapshot_id: RepoSnapshotId,
    pub native_object_format: HashAlgorithm,
    pub import_policy: ImportPolicy,
    pub refs: Vec<PersistedRef>,
    pub summary: ImportSummary,
    pub imported_unix_secs: u64,
    pub tool_version: Vec<u8>,
    pub optional_fields: Vec<OptionalManifestField>,
}

impl SnapshotManifest {
    /// Builds a manifest from a successfully imported immutable ref snapshot.
    pub fn from_import(
        repository: RepoId,
        snapshot_id: RepoSnapshotId,
        result: &ImportResult,
        imported_unix_secs: u64,
        tool_version: impl AsRef<[u8]>,
    ) -> Result<Self, SnapshotManifestError> {
        let manifest = Self {
            repository,
            snapshot_id,
            native_object_format: result.snapshot.algorithm,
            import_policy: ImportPolicy::LocalBranchesAndTags,
            refs: result
                .snapshot
                .refs
                .iter()
                .map(|reference| PersistedRef {
                    name: reference.name.as_bytes().to_vec(),
                    kind: reference.kind,
                    target: reference.target,
                })
                .collect(),
            summary: result.summary,
            imported_unix_secs,
            tool_version: tool_version.as_ref().to_vec(),
            optional_fields: Vec::new(),
        };
        manifest.validate()?;
        Ok(manifest)
    }

    /// Adds opaque optional data. Duplicate tags are rejected because a
    /// future reader could otherwise give an ambiguous meaning to a field.
    pub fn add_optional_field(
        &mut self,
        field: OptionalManifestField,
    ) -> Result<(), SnapshotManifestError> {
        if self
            .optional_fields
            .iter()
            .any(|existing| existing.tag == field.tag)
        {
            return Err(SnapshotManifestError::Invalid(
                "snapshot manifest repeats an optional field tag",
            ));
        }
        if field.tag == 0 {
            return Err(SnapshotManifestError::Invalid(
                "snapshot manifest optional field tag is zero",
            ));
        }
        if field.value.len() > MANIFEST_OPTIONAL_FIELD_MAX_BYTES {
            return Err(SnapshotManifestError::Invalid(
                "snapshot manifest optional field is too large",
            ));
        }
        self.optional_fields.push(field);
        Ok(())
    }

    fn validate(&self) -> Result<(), SnapshotManifestError> {
        if self.tool_version.is_empty() || self.tool_version.len() > MANIFEST_TOOL_VERSION_MAX_BYTES
        {
            return Err(SnapshotManifestError::Invalid(
                "snapshot manifest tool version is empty or too large",
            ));
        }
        if self
            .summary
            .objects_written
            .checked_add(self.summary.objects_deduplicated)
            != Some(self.summary.objects_seen)
        {
            return Err(SnapshotManifestError::Invalid(
                "snapshot manifest import counters are inconsistent",
            ));
        }
        if self.refs.len() > u32::MAX as usize {
            return Err(SnapshotManifestError::Invalid(
                "snapshot manifest has too many refs",
            ));
        }
        let mut previous_name = None;
        for reference in &self.refs {
            if reference.name.is_empty() || reference.name.len() > MANIFEST_REF_NAME_MAX_BYTES {
                return Err(SnapshotManifestError::Invalid(
                    "snapshot manifest ref name is empty or too large",
                ));
            }
            if reference.target.algorithm() != self.native_object_format {
                return Err(SnapshotManifestError::Invalid(
                    "snapshot manifest ref OID algorithm disagrees with native object format",
                ));
            }
            let namespace = match reference.kind {
                LocalRefKind::Branch => b"refs/heads/".as_slice(),
                LocalRefKind::Tag => b"refs/tags/".as_slice(),
            };
            if !reference.name.starts_with(namespace) {
                return Err(SnapshotManifestError::Invalid(
                    "snapshot manifest ref is outside its local namespace",
                ));
            }
            if let Some(previous_name) = previous_name {
                if previous_name >= reference.name.as_slice() {
                    return Err(SnapshotManifestError::Invalid(
                        "snapshot manifest refs are not strictly byte sorted",
                    ));
                }
            }
            previous_name = Some(reference.name.as_slice());
        }
        if self.optional_fields.len() > u32::MAX as usize {
            return Err(SnapshotManifestError::Invalid(
                "snapshot manifest has too many optional fields",
            ));
        }
        for (index, field) in self.optional_fields.iter().enumerate() {
            if field.tag == 0 || field.value.len() > MANIFEST_OPTIONAL_FIELD_MAX_BYTES {
                return Err(SnapshotManifestError::Invalid(
                    "snapshot manifest contains an invalid optional field",
                ));
            }
            if self.optional_fields[..index]
                .iter()
                .any(|prior| prior.tag == field.tag)
            {
                return Err(SnapshotManifestError::Invalid(
                    "snapshot manifest repeats an optional field tag",
                ));
            }
        }
        Ok(())
    }
}

/// Failure while encoding, publishing, or loading a snapshot manifest.
#[derive(Debug)]
pub enum SnapshotManifestError {
    Io(io::Error),
    Invalid(&'static str),
    UnsupportedVersion(u16),
    LengthOverflow,
}

impl std::fmt::Display for SnapshotManifestError {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        match self {
            Self::Io(error) => write!(f, "snapshot manifest I/O error: {error}"),
            Self::Invalid(reason) => write!(f, "invalid snapshot manifest: {reason}"),
            Self::UnsupportedVersion(version) => {
                write!(f, "unsupported snapshot manifest version {version}")
            }
            Self::LengthOverflow => write!(f, "snapshot manifest length overflow"),
        }
    }
}
impl std::error::Error for SnapshotManifestError {
    fn source(&self) -> Option<&(dyn std::error::Error + 'static)> {
        match self {
            Self::Io(error) => Some(error),
            _ => None,
        }
    }
}
impl From<io::Error> for SnapshotManifestError {
    fn from(value: io::Error) -> Self {
        Self::Io(value)
    }
}

#[derive(Clone, Copy, Debug, Default, Eq, PartialEq)]
pub struct ImportSummary {
    pub objects_seen: u64,
    pub objects_written: u64,
    pub objects_deduplicated: u64,
    pub raw_bytes: u64,
}
#[derive(Clone, Debug, Eq, PartialEq)]
pub struct ImportResult {
    pub snapshot: RefSnapshot,
    pub summary: ImportSummary,
}
#[derive(Debug)]
pub enum ImportError {
    Git(GitBackendError),
    Store(StoreError),
    LengthOverflow,
}
impl std::fmt::Display for ImportError {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        match self {
            Self::Git(error) => write!(f, "Git import error: {error}"),
            Self::Store(error) => write!(f, "cold-store import error: {error}"),
            Self::LengthOverflow => write!(f, "import counter overflow"),
        }
    }
}
impl std::error::Error for ImportError {}
impl From<GitBackendError> for ImportError {
    fn from(value: GitBackendError) -> Self {
        Self::Git(value)
    }
}
impl From<StoreError> for ImportError {
    fn from(value: StoreError) -> Self {
        Self::Store(value)
    }
}

/// Writes a complete repository snapshot manifest using the durable
/// temporary-file, sync, rename, and parent-directory-sync sequence.
///
/// Replacing an existing manifest is atomic. A failed write never makes the
/// temporary file a visible manifest, although the old manifest remains until
/// the rename succeeds.
pub fn write_snapshot_manifest(
    path: impl AsRef<Path>,
    manifest: &SnapshotManifest,
) -> Result<(), SnapshotManifestError> {
    manifest.validate()?;
    let bytes = encode_snapshot_manifest(manifest)?;
    let path = path.as_ref();
    let parent = path.parent().unwrap_or_else(|| Path::new("."));
    let (temporary, mut file) = create_manifest_temporary_file(parent)?;
    let result = (|| -> Result<(), SnapshotManifestError> {
        file.write_all(&bytes)?;
        file.sync_all()?;
        drop(file);
        fs::rename(&temporary, path)?;
        File::open(parent)?.sync_all()?;
        Ok(())
    })();
    if result.is_err() {
        let _ = fs::remove_file(&temporary);
    }
    result
}

/// Reads and fully validates a repository snapshot manifest. Unknown optional
/// fields remain in [`SnapshotManifest::optional_fields`] so callers can
/// retain them on a later rewrite.
pub fn read_snapshot_manifest(
    path: impl AsRef<Path>,
) -> Result<SnapshotManifest, SnapshotManifestError> {
    let mut file = File::open(path)?;
    let length = usize::try_from(file.metadata()?.len())
        .map_err(|_| SnapshotManifestError::LengthOverflow)?;
    if length > MAX_SNAPSHOT_MANIFEST_BYTES {
        return Err(SnapshotManifestError::Invalid(
            "snapshot manifest exceeds the maximum size",
        ));
    }
    let mut bytes = Vec::with_capacity(length);
    file.read_to_end(&mut bytes)?;
    if bytes.len() != length {
        return Err(SnapshotManifestError::Invalid(
            "snapshot manifest changed while being read",
        ));
    }
    decode_snapshot_manifest(&bytes)
}

fn encode_snapshot_manifest(manifest: &SnapshotManifest) -> Result<Vec<u8>, SnapshotManifestError> {
    manifest.validate()?;
    let mut bytes = Vec::new();
    bytes.extend_from_slice(SNAPSHOT_MANIFEST_MAGIC);
    put_u16(&mut bytes, SNAPSHOT_MANIFEST_VERSION);
    bytes.extend_from_slice(&manifest.repository.0);
    bytes.extend_from_slice(&manifest.snapshot_id.0);
    bytes.push(manifest.native_object_format.tag());
    bytes.push(import_policy_tag(manifest.import_policy));
    put_u16(&mut bytes, 0); // required reserved bytes
    put_u64(&mut bytes, manifest.imported_unix_secs);
    put_u64(&mut bytes, manifest.summary.objects_seen);
    put_u64(&mut bytes, manifest.summary.objects_written);
    put_u64(&mut bytes, manifest.summary.objects_deduplicated);
    put_u64(&mut bytes, manifest.summary.raw_bytes);
    put_u16(
        &mut bytes,
        u16::try_from(manifest.tool_version.len())
            .map_err(|_| SnapshotManifestError::LengthOverflow)?,
    );
    put_u32(
        &mut bytes,
        u32::try_from(manifest.refs.len()).map_err(|_| SnapshotManifestError::LengthOverflow)?,
    );
    put_u32(
        &mut bytes,
        u32::try_from(manifest.optional_fields.len())
            .map_err(|_| SnapshotManifestError::LengthOverflow)?,
    );
    bytes.extend_from_slice(&manifest.tool_version);
    for reference in &manifest.refs {
        bytes.push(local_ref_kind_tag(reference.kind));
        put_u16(
            &mut bytes,
            u16::try_from(reference.name.len())
                .map_err(|_| SnapshotManifestError::LengthOverflow)?,
        );
        bytes.extend_from_slice(&reference.name);
        bytes.push(reference.target.algorithm().tag());
        bytes.push(reference.target.len());
        bytes.extend_from_slice(reference.target.as_bytes());
    }
    for field in &manifest.optional_fields {
        put_u16(&mut bytes, field.tag);
        put_u32(
            &mut bytes,
            u32::try_from(field.value.len()).map_err(|_| SnapshotManifestError::LengthOverflow)?,
        );
        bytes.extend_from_slice(&field.value);
    }
    if bytes.len() > MAX_SNAPSHOT_MANIFEST_BYTES {
        return Err(SnapshotManifestError::Invalid(
            "snapshot manifest exceeds the maximum size",
        ));
    }
    Ok(bytes)
}

fn decode_snapshot_manifest(bytes: &[u8]) -> Result<SnapshotManifest, SnapshotManifestError> {
    if bytes.len() > MAX_SNAPSHOT_MANIFEST_BYTES {
        return Err(SnapshotManifestError::Invalid(
            "snapshot manifest exceeds the maximum size",
        ));
    }
    let mut reader = ManifestReader::new(bytes);
    if reader.take(8)? != SNAPSHOT_MANIFEST_MAGIC {
        return Err(SnapshotManifestError::Invalid(
            "snapshot manifest has an invalid magic value",
        ));
    }
    let version = reader.u16()?;
    if version != SNAPSHOT_MANIFEST_VERSION {
        return Err(SnapshotManifestError::UnsupportedVersion(version));
    }
    let repository = RepoId(reader.array::<16>()?);
    let snapshot_id = RepoSnapshotId(reader.array::<REPO_SNAPSHOT_ID_LEN>()?);
    let native_object_format = HashAlgorithm::from_tag(reader.u8()?).ok_or(
        SnapshotManifestError::Invalid("snapshot manifest has an invalid native object format"),
    )?;
    let import_policy = import_policy_from_tag(reader.u8()?)?;
    if reader.u16()? != 0 {
        return Err(SnapshotManifestError::Invalid(
            "snapshot manifest has non-zero required reserved bytes",
        ));
    }
    let imported_unix_secs = reader.u64()?;
    let summary = ImportSummary {
        objects_seen: reader.u64()?,
        objects_written: reader.u64()?,
        objects_deduplicated: reader.u64()?,
        raw_bytes: reader.u64()?,
    };
    let tool_version_length = usize::from(reader.u16()?);
    if tool_version_length > MANIFEST_TOOL_VERSION_MAX_BYTES {
        return Err(SnapshotManifestError::Invalid(
            "snapshot manifest tool version is too large",
        ));
    }
    let ref_count =
        usize::try_from(reader.u32()?).map_err(|_| SnapshotManifestError::LengthOverflow)?;
    let optional_field_count =
        usize::try_from(reader.u32()?).map_err(|_| SnapshotManifestError::LengthOverflow)?;
    let tool_version = reader.take(tool_version_length)?.to_vec();
    let mut refs = Vec::with_capacity(ref_count.min(4096));
    for _ in 0..ref_count {
        let kind = local_ref_kind_from_tag(reader.u8()?)?;
        let name_length = usize::from(reader.u16()?);
        if name_length > MANIFEST_REF_NAME_MAX_BYTES {
            return Err(SnapshotManifestError::Invalid(
                "snapshot manifest ref name is too large",
            ));
        }
        let name = reader.take(name_length)?.to_vec();
        let algorithm = HashAlgorithm::from_tag(reader.u8()?).ok_or(
            SnapshotManifestError::Invalid("snapshot manifest has an invalid ref OID algorithm"),
        )?;
        let oid_length = reader.u8()?;
        if oid_length != algorithm.oid_len() {
            return Err(SnapshotManifestError::Invalid(
                "snapshot manifest has an invalid ref OID length",
            ));
        }
        let target =
            GitOid::new(algorithm, reader.take(usize::from(oid_length))?).map_err(|_| {
                SnapshotManifestError::Invalid("snapshot manifest has an invalid ref OID")
            })?;
        refs.push(PersistedRef { name, kind, target });
    }
    let mut optional_fields = Vec::with_capacity(optional_field_count.min(64));
    for _ in 0..optional_field_count {
        let tag = reader.u16()?;
        let length =
            usize::try_from(reader.u32()?).map_err(|_| SnapshotManifestError::LengthOverflow)?;
        if length > MANIFEST_OPTIONAL_FIELD_MAX_BYTES {
            return Err(SnapshotManifestError::Invalid(
                "snapshot manifest optional field is too large",
            ));
        }
        optional_fields.push(OptionalManifestField {
            tag,
            value: reader.take(length)?.to_vec(),
        });
    }
    if !reader.is_empty() {
        return Err(SnapshotManifestError::Invalid(
            "snapshot manifest has trailing bytes",
        ));
    }
    let manifest = SnapshotManifest {
        repository,
        snapshot_id,
        native_object_format,
        import_policy,
        refs,
        summary,
        imported_unix_secs,
        tool_version,
        optional_fields,
    };
    manifest.validate()?;
    Ok(manifest)
}

fn create_manifest_temporary_file(
    parent: &Path,
) -> Result<(std::path::PathBuf, File), SnapshotManifestError> {
    for attempt in 0..32_u32 {
        let timestamp = SystemTime::now()
            .duration_since(UNIX_EPOCH)
            .map_err(|_| SnapshotManifestError::Invalid("system clock is before the Unix epoch"))?
            .as_nanos();
        let temporary = parent.join(format!(
            ".reflink-forest-snapshot-{}-{timestamp}-{attempt}",
            process::id()
        ));
        match OpenOptions::new()
            .write(true)
            .create_new(true)
            .open(&temporary)
        {
            Ok(file) => return Ok((temporary, file)),
            Err(error) if error.kind() == io::ErrorKind::AlreadyExists => continue,
            Err(error) => return Err(SnapshotManifestError::Io(error)),
        }
    }
    Err(SnapshotManifestError::Invalid(
        "cannot allocate a temporary snapshot manifest path",
    ))
}

fn import_policy_tag(policy: ImportPolicy) -> u8 {
    match policy {
        ImportPolicy::LocalBranchesAndTags => 1,
    }
}

fn import_policy_from_tag(tag: u8) -> Result<ImportPolicy, SnapshotManifestError> {
    match tag {
        1 => Ok(ImportPolicy::LocalBranchesAndTags),
        _ => Err(SnapshotManifestError::Invalid(
            "snapshot manifest has an unsupported import policy",
        )),
    }
}

fn local_ref_kind_tag(kind: LocalRefKind) -> u8 {
    match kind {
        LocalRefKind::Branch => 1,
        LocalRefKind::Tag => 2,
    }
}

fn local_ref_kind_from_tag(tag: u8) -> Result<LocalRefKind, SnapshotManifestError> {
    match tag {
        1 => Ok(LocalRefKind::Branch),
        2 => Ok(LocalRefKind::Tag),
        _ => Err(SnapshotManifestError::Invalid(
            "snapshot manifest has an invalid ref kind",
        )),
    }
}

fn put_u16(bytes: &mut Vec<u8>, value: u16) {
    bytes.extend_from_slice(&value.to_be_bytes());
}
fn put_u32(bytes: &mut Vec<u8>, value: u32) {
    bytes.extend_from_slice(&value.to_be_bytes());
}
fn put_u64(bytes: &mut Vec<u8>, value: u64) {
    bytes.extend_from_slice(&value.to_be_bytes());
}

struct ManifestReader<'a> {
    bytes: &'a [u8],
    offset: usize,
}
impl<'a> ManifestReader<'a> {
    fn new(bytes: &'a [u8]) -> Self {
        Self { bytes, offset: 0 }
    }
    fn take(&mut self, length: usize) -> Result<&'a [u8], SnapshotManifestError> {
        let end = self
            .offset
            .checked_add(length)
            .ok_or(SnapshotManifestError::LengthOverflow)?;
        let value = self
            .bytes
            .get(self.offset..end)
            .ok_or(SnapshotManifestError::Invalid(
                "snapshot manifest is truncated",
            ))?;
        self.offset = end;
        Ok(value)
    }
    fn array<const N: usize>(&mut self) -> Result<[u8; N], SnapshotManifestError> {
        self.take(N)?.try_into().map_err(|_| {
            SnapshotManifestError::Invalid("snapshot manifest fixed field is truncated")
        })
    }
    fn u8(&mut self) -> Result<u8, SnapshotManifestError> {
        Ok(self.take(1)?[0])
    }
    fn u16(&mut self) -> Result<u16, SnapshotManifestError> {
        Ok(u16::from_be_bytes(self.array()?))
    }
    fn u32(&mut self) -> Result<u32, SnapshotManifestError> {
        Ok(u32::from_be_bytes(self.array()?))
    }
    fn u64(&mut self) -> Result<u64, SnapshotManifestError> {
        Ok(u64::from_be_bytes(self.array()?))
    }
    fn is_empty(&self) -> bool {
        self.offset == self.bytes.len()
    }
}

/// Imports local branch/tag closure. Publication of a repository `Ready`
/// snapshot is deliberately a separate durable manifest transaction after this
/// function completes without error.
pub fn import_local_refs<B: GitBackend, C: Catalog>(
    backend: &B,
    writer: &mut ChunkWriter,
    catalog: &mut C,
    repo: RepoId,
    generation: u32,
    chunk_id: u64,
) -> Result<ImportResult, ImportError> {
    let snapshot = backend.snapshot_local_refs()?;
    let objects = backend.reachable_objects(&snapshot.roots())?;
    let mut summary = ImportSummary::default();
    for object in objects {
        summary.objects_seen = summary
            .objects_seen
            .checked_add(1)
            .ok_or(ImportError::LengthOverflow)?;
        summary.raw_bytes = summary
            .raw_bytes
            .checked_add(u64::try_from(object.data.len()).map_err(|_| ImportError::LengthOverflow)?)
            .ok_or(ImportError::LengthOverflow)?;
        let content_id = ContentId::for_object(object.kind, &object.data);
        let record = ObjectRecord {
            kind: object.kind,
            codec: Codec::Raw,
            flags: 0,
            raw_length: u64::try_from(object.data.len())
                .map_err(|_| ImportError::LengthOverflow)?,
            content_id,
            primary_oid: object.oid,
            payload: object.data,
        };
        if writer
            .append_and_index(catalog, repo, generation, chunk_id, &record)?
            .is_some()
        {
            summary.objects_written = summary
                .objects_written
                .checked_add(1)
                .ok_or(ImportError::LengthOverflow)?;
        } else {
            summary.objects_deduplicated = summary
                .objects_deduplicated
                .checked_add(1)
                .ok_or(ImportError::LengthOverflow)?;
        }
    }
    Ok(ImportResult { snapshot, summary })
}

